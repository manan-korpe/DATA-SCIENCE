l1 = ["a", "b", "c" , "c", 10, 2.5, True]

print(l1[0])    # First element
print(l1[3])    # Fourth element (index starts at 0)    
print(l1[-1])   # Last element
print(l1[-3])   # Third last element
print(l1[1:4])  # Elements from index 1 to 3
print(l1[2:])   # Elements from index 2 to end
print(l1[:3])   # Elements from start to index 2
print(l1[:])    # All elements
print(l1[::2])  # Every second element
print(l1[::-1]) # Reversed list